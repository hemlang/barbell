function rotr(x, n) {
    return ((x >>> n) | (x << (32 - n))) >>> 0;
}

function sha256Transform(state, block) {
    const W = new Array(64);

    // Prepare message schedule
    for (let i = 0; i < 16; i++) {
        W[i] = (block[i * 4] << 24) | (block[i * 4 + 1] << 16) | (block[i * 4 + 2] << 8) | block[i * 4 + 3];
        W[i] = W[i] >>> 0;
    }

    for (let i = 16; i < 64; i++) {
        const s0 = rotr(W[i - 15], 7) ^ rotr(W[i - 15], 18) ^ (W[i - 15] >>> 3);
        const s1 = rotr(W[i - 2], 17) ^ rotr(W[i - 2], 19) ^ (W[i - 2] >>> 10);
        W[i] = (W[i - 16] + s0 + W[i - 7] + s1) >>> 0;
    }

    let [a, b, c, d, e, f, g, h] = state;

    for (let i = 0; i < 64; i++) {
        const S1 = rotr(e, 6) ^ rotr(e, 11) ^ rotr(e, 25);
        const ch = (e & f) ^ (~e & g);
        const temp1 = (h + S1 + ch + K[i] + W[i]) >>> 0;
        const S0 = rotr(a, 2) ^ rotr(a, 13) ^ rotr(a, 22);
        const maj = (a & b) ^ (a & c) ^ (b & c);
        const temp2 = (S0 + maj) >>> 0;

        h = g;
        g = f;
        f = e;
        e = (d + temp1) >>> 0;
        d = c;
        c = b;
        b = a;
        a = (temp1 + temp2) >>> 0;
    }

    return [
        (state[0] + a) >>> 0,
        (state[1] + b) >>> 0,
        (state[2] + c) >>> 0,
        (state[3] + d) >>> 0,
        (state[4] + e) >>> 0,
        (state[5] + f) >>> 0,
        (state[6] + g) >>> 0,
        (state[7] + h) >>> 0
    ];
}

function sha256(data) {
    let state = [
        0x6a09e667, 0xbb67ae85, 0x3c6ef372, 0xa54ff53a,
        0x510e527f, 0x9b05688c, 0x1f83d9ab, 0x5be0cd19
    ];

    const length = data.length;
    const bits = length * 8;

    // Process full blocks
    let i = 0;
    while (i + 64 <= length) {
        state = sha256Transform(state, data.slice(i, i + 64));
        i += 64;
    }

    // Padding
    const remaining = data.slice(i);
    let block = [...remaining, 0x80, ...new Array(55 - remaining.length).fill(0)];

    if (remaining.length >= 56) {
        block = [...remaining, 0x80, ...new Array(63 - remaining.length).fill(0)];
        state = sha256Transform(state, block);
        block = new Array(56).fill(0);
    }

    // Append length
    block.push(
        0, 0, 0, 0,  // High 32 bits (assuming length < 2^32)
        (bits >>> 24) & 0xff,
        (bits >>> 16) & 0xff,
        (bits >>> 8) & 0xff,
        bits & 0xff
    );
    state = sha256Transform(state, block);

    // Output hash
    const result = [];
    for (const s of state) {
        result.push(
            (s >>> 24) & 0xff,
            (s >>> 16) & 0xff,
            (s >>> 8) & 0xff,
            s & 0xff
        );
    }
    return result;
}

const iterations = parseInt(process.argv[2]) || 100000;
const message = "benchmark test message for sha256 hashing!!!!!!!";
const data = new Array(64).fill(0);
for (let i = 0; i < message.length; i++) {
    data[i] = message.charCodeAt(i);
}

const start = performance.now();

let hashResult;
for (let i = 0; i < iterations; i++) {
    hashResult = sha256(data.slice(0, 48));
    for (let j = 0; j < 32; j++) {
        data[j] = hashResult[j];
    }
}

// Print checksum
const checksum = hashResult.reduce((a, b) => a + b, 0);

const elapsed = performance.now() - start;
console.error(`TIME_MS:${elapsed.toFixed(2)}`);
